#This method is used to capitalize 1st letter of sentence
def disp():
    sentence1='i am komolika, god is with me in my journy.'
    after_capitalization=sentence1.capitalize()
    print('capitalization:',after_capitalization)
disp()

def show():
    sen2=input('Enter your sentence:')
    cap=sen2.capitalize()
    print('Output is:',cap)
show()

